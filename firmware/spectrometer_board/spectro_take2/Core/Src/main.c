static float readvoltage(void)
{
	uint32_t raw_val = 0;


	if (HAL_ADC_Start(&hadc1) != HAL_OK) {
		return 0.0f;
	}

	if (HAL_ADC_PollForConversion(&hadc1, 10) != HAL_OK) {
		HAL_ADC_Stop(&hadc1);
		return 0.0f;
	}

	raw_val = HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);

	float voltage = ((float)raw_val / adc_max_counts) * adc_ref_voltage;
	return voltage;

}

static void sendsample(float voltage)
{
	uint8_t buf[4];

	union {
	float f;
	uint8_t b[4];
	} u;
	u.f = voltage;

	buf[0] = u.b[0];
	buf[1] = u.b[1];
	buf[2] = u.b[2];
	buf[3] = u.b[3];

	if (FDCAN_Transmit(&hfdcan1, spectro_can_id, buf, 4) != HAL_OK) {
		HAL_GPIO_WritePin(DEBUG_LED_2_GPIO_Port, DEBUG_LED_2_Pin, GPIO_PIN_SET);
	}

}

